package com.abhishek.prep_samples2022;

public class B {
	public static void B1(){
		  System.out.println("I am class B-public static method");
	  }
	  
	  public void B2() {
		  System.out.println("I am class B-public instance method");
	  }
	  
	  protected static void B3() {
		  System.out.println("I am class B-protected static method");
	  }
	  
	 protected void B4() {
		 System.out.println("I am class B-protected instance method");
	  }
	 
	 private static void B5() {
		 System.out.println("I am class B-private static method");
	 }
	 
	 private void B6() {
		 System.out.println("I am class B-private instance method");
	 }
}